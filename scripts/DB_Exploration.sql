-- ###############################################
-- Dimensions Exploration
-- ###############################################

-- Explore All Countries our customers come from.
SELECT DISTINCT country
FROM gold.dim_customers

-- Explore All Categories "The Major Divisions"
SELECT DISTINCT category
FROM gold.dim_products

-- ###############################################
-- Date Exploration
-- ###############################################

-- Find the date of the first and last orders
SELECT
	MIN(order_date) AS first_order_date
	, MAX(order_date) AS last_order_date
	, DATEDIFF(MONTH, MIN(order_date), MAX(order_date)) AS order_range_months
FROM gold.fact_sales

-- Find the youngest and the oldest customers
SELECT
	MIN(birthdate) AS oldest_customer
	, DATEDIFF(YEAR, MIN(birthdate), GETUTCDATE()) oldest_customer_years
	, MAX(birthdate) AS youngest_customer
	, DATEDIFF(YEAR, MAX(birthdate), GETUTCDATE()) youngest_customer_years
FROM gold.dim_customers

